import sqlite3
import random
import pandas as pd
import os

def create_faculty_database():
    db_file = 'faculty.db'
    if os.path.exists(db_file):
        os.remove(db_file)

    conn = sqlite3.connect(db_file)
    cursor = conn.cursor()

    cursor.execute('DROP TABLE IF EXISTS faculty')

    cursor.execute('''
        CREATE TABLE IF NOT EXISTS faculty(
            id INTEGER PRIMARY KEY,
            name TEXT,
            assigned_subject TEXT,
            experience INTEGER,
            salary REAL
        )
    ''')

    subjects = ['Computer Science', 'Electrical Engineering', 'Mechanical Engineering']

    faculty_data = [
        ("Dr. Alice Smith", random.choice(subjects), random.randint(5, 20), round(random.uniform(60000, 120000), 2)),
        ("Dr. Bob Johnson", random.choice(subjects), random.randint(3, 15), round(random.uniform(50000, 100000), 2)),
        ("Dr. Carol White", random.choice(subjects), random.randint(10, 25), round(random.uniform(70000, 150000), 2))
    ]

    for i, (name, subject, experience, salary) in enumerate(faculty_data):
        cursor.execute(
            "INSERT INTO faculty (id, name, assigned_subject, experience, salary) VALUES (?, ?, ?, ?, ?)",
            (i + 1, name, subject, experience, salary)
        )

    conn.commit()
    cursor.execute("SELECT * FROM faculty")
    rows = cursor.fetchall()
    df_faculty = pd.DataFrame(rows, columns=['id', 'name', 'assigned_subject', 'experience', 'salary'])
    conn.close()
    return df_faculty

faculty_data_df = create_faculty_database()

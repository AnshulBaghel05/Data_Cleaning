import sqlite3
import random
import pandas as pd
import numpy as np
import os

def create_new_database():
    db_file = 'new_example.db'
    if os.path.exists(db_file):
        os.remove(db_file)

    conn = sqlite3.connect(db_file)
    cursor = conn.cursor()

    cursor.execute('DROP TABLE IF EXISTS students')

    cursor.execute('''
        CREATE TABLE IF NOT EXISTS students(
            id INTEGER PRIMARY KEY,
            name TEXT,
            course TEXT,
            cgpa REAL,
            email_id TEXT
        )
    ''')

    courses = ['Computer Science', 'Electrical Engineering', 'Mechanical Engineering']

    for i in range(1, 101):
        name = f"Student_{i}"
        course = random.choice(courses)
        cgpa = round(random.uniform(2.0, 4.0), 2)
        email_id = f"student{i}@example.com"

        cursor.execute(
            "INSERT INTO students (id, name, course, cgpa, email_id) VALUES (?, ?, ?, ?, ?)",
            (i, name, course, cgpa, email_id)
        )

    conn.commit()
    cursor.execute("SELECT * FROM students")
    rows = cursor.fetchall()
    df_new = pd.DataFrame(rows, columns=['id', 'name', 'course', 'cgpa', 'email_id'])
    conn.close()
    return df_new


